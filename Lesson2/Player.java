public class Player {
    private String name;
    private int number;

    public Player() {
        String name = "Bob";
        this.name = name;
        this.number = number;
    }

    public void setName() {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setNumber() {
        this.number = number;
    }

    public int getNumber() {
        return number;
    }
}
